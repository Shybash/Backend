const mongoose=require('mongoose');

const CollegeSchema=new mongoose.Schema({
    username:{
        type:String,
        required:true
    },
    email:{
        type:String,
        required:true,
        unique:true
    },
    password:{
        type:String,
        required:true
    },
    confirmpassword:{
        type:String,
        required:false

    }
});


module.exports=mongoose.model('College',CollegeSchema);